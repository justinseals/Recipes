			<h2>Instructions</h2>
			<p><strong>Prepare (6min):</strong></p>
			<ol>
				<li>Pre heat oven to 240 degrees</li>
				<li>Peel and slice potatoes</li>
				<li>Finely chop rosemary, sprinkle into the pack of chicken, sprinkle 1 teaspoon of mustard powder on each breast, season, drizzle oil and rub into chicken</li>
			</ol>
			<p><strong>Cooking (60min):</strong></p>
			<ol>
				<li>Put potatoes with a drizzle of oil, salt, pepper and oregano into the pyrex and into the oven (60min total)</li>
				<li>Turn potatoes and re-drizzle with oil at the 30min mark</li>
				<li>Heat up the frying pan, add the chicken breasts (turn once, 6 mins each side)</li>
				<li>Cut the leeks lengthways, wash and slice finely, add to the other side of the pan than the chicken</li>
				<li>Put on the vegetables</li>
				<li>Crush 4 unpeeled cloves of garlic into the chicken, stir the leeks and add a good swig of wine</li>
				<li>Check chicken is cooked, if so, add 75ml of cream, turn the head off, stir and cover</li>
				<li>Take out the chicken breasts, slice on a board</li>
				<li>Add 1 heaped teaspoon of wholegrain mustard into the remaining sauce and stir, season if necessery and serve</li>
			</ol>
