			<p><strong>Food:</strong></p>
			<ul>
				<li>500g Dark Belgium cooking Chocolate</li>
				<li>5 Large eggs</li>
				<li>75ml of double cream</li>
				<li>2 tablespoons of castor sugar</li>
				<li>1 teaspoon of vanilla extract</li>
			</ul>
			<p><strong>Utensils:</strong></p>
			<ul>
				<li>2 large glass bowls (1 heatproof)</li>
				<li>Electric whisk</li>
				<li>Saucepan</li>
			</ul>
		</div><!--Ingredients End-->
		
		<div id="instructions">
			<h2>Instructions</h2>
			<p><strong>Prepare (2min):</strong></p>
			<ol>
				<li>Boil the kettle</li>
				<li>Break up chocolate into small pieces, put in the heatproof bowl and add 6 tablespoons of water</li>
			</ol>
			<p><strong>Cooking (20min):</strong></p>
			<ol>
				<li>Place bowl of chocolate over simmering water and stir as it melts (approx 5 minutes) - when melted take off the heat</li>
				<li>Seperate all the eggs, add 1 tablespoon of vanilla extract to egg yokes</li>
				<li>Put the cream and sugar in another bowl</li>
				<li>Whisk egg whites until very stiff</li>
				<li>Whisk sugar and cream until thick</li>
				<li>Mix/whisk egg yokes with vanilla</li>
				<li>Add egg yokes gradually to melted chocolate and keep stirring to keep smooth</li>
				<li>Add cream mixture and stir until all one colour</li>
				<li>Fold in the beaten egg whites until all one colour keeping as much air in the mixture as possible (no stirring)</li>
				<li>Pour mixture into serving bowl and refridgerate for a minimum of 2 hours</li>
			</ol>
