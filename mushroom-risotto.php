		<div id="instructions">
			<h2>Instructions</h2>
			<p><strong>Prepare (2min):</strong></p>
			<ol>
				<li>Put the onion, celery and porcini mushrooms into a food blender until finely chopped</li>
				<li>Get 1l of boiling stock ready</li>
				<li>Put the grill on</li>
			</ol>
			<p><strong>Cooking (20min):</strong></p>
			<ol>
				<li>Put everything from the food processor into the saucepan with a couple of lugs of oil</li>
				<li>Finely chop the rosemary leaves and add to saucepan with the rice, stir for 1 min</li>
				<li>Then pour in the wine and crumble the stock cube, stir until wine is absorbed</li>
				<li>Season, and then add a mug of boiling water, keep adding water every minute for the 18 minutes cooking time (1 litre total)</li>
				<li>Tear half the mushrooms and put into the risotto, fry the other half seperately</li>
				<li>Crush over 1 unpeeled clove of garlic into the fried mushrooms and add some of the fresh thyme, add the rest to the risotto</li>
				<li>Put the fried mushrooms in the grill for a few minutes to crisp up</li>
				<li>Stir in the butter, fineraly grate over most of the parmesan, add squeeze of lemon juice, season with salt and pepper and add a splash more water</li>
				<li>Chop the parsely and sprinle 1/2 over the risotto and 1/2 over the crispy mushrooms</li> 				
			</ol>
