			<ol>
				<li>Heat oven to 175 degrees. Line an 8x8-inch square baking pan with parchment, extending it up two sides, or foil. Butter the parchment or foil or spray it with a nonstick cooking spray.</li>
			</ol>
			<p><strong>Cooking (35min):</strong></p>
			<ol>
				<li>Melt chocolate over gently simmering water until only a few small chunks remain, take off the head and stir until fully melted</li>
				<li>Whisk in sugar, then eggs one at a time, then vanilla and salt</li>
				<li>Stir in flour with a spoon or flexible spatula and scrape batter into a prepared baking pan, spread until even</li>
				<li>Bake for 28 minutes or until a toothpick inserted into the center comes out batter-free</li>
				<li>Dust with powdered sugar and serve</li>
			</ol>
