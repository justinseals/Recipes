			<p><strong>Prepare (2min):</strong></p>
			<ol>
				<li>Cut off the ends to the courgettes and then slice into 1cm thick slices</li>
				<li>Cut onion into large chunks</li>
				<li>Boil a kettle</li>
			</ol>
			<p><strong>Cooking (50min):</strong></p>
			<ol>
				<li>Put the courgettes, onion, heaped tablespoon of Telma and heaped tablespoon of Philadelphia cheese into the saucepan and fill with water so it just covers the top of the tile of courgettes/onion</li>
				<li>Let simmer for 45 minutes with a lid mainly on (at a slight angle)</li>
				<li>Take off the heat, blend and add any salt/pepper/chinese 5 spice as you like</li>
			</ol>
