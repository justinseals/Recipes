			<h2>Instructions</h2>
			<p><strong>Prepare (10min):</strong></p>
			<ol>
				<li>Boil the kettle</li>
				<li>Put the meat on a wooden board, sprinkle over salt, pepper, pounded Szechuan pepper, good pinch of Chinese 5 spice, olive oil and rub into meat</li>
				<li>Put the chilli oil, soy sauce, crush the clove of garlic, finely chopped spring onions all in the serving bowl and mix</li>
				<li>Peel the ginger, cut the chilli finely and 1 clove of garlic</li>
			</ol>
			<p><strong>Cooking (10min):</strong></p>
			<ol>
				<li>Put on the vegetables and reboil the kettle</li>
				<li>Fry the steaks, 15 seconds on each side and then 2 minutes on each side depending on the thickness</li>
				<li>Take the steaks out, put them on a clean board, drizzle olive oil over, grate the ginger over the steaks, add the chilli and garlic over the top, squeeze over the lime juice</li>
				<li>Add the noodles to a saucepan with the stock cube</li>
				<li>Squeeze some lemon and add some honey to the mixing bowl</li>
				<li>Slice the steaks at an angle, toss so they mop up the flavours, tear over the coriander</li>
				<li>Drain the noodles and pour into mixing bowl, toss very thoughrouly, pour some of the broth over if any left</li>
			</ol>
