				<li>500g of mussels</li>
				<li>1/2 cup dry white wine</li>
				<li>1/2 teaspoon saffron threads, crushed</li>
				<li>175ml whipping cream</li>
				<li>120ml canned crushed tomatoes with added puree</li>
				<li>1 garlic clove</li>
				<li>1 bay leaf</li>
				<li>1/8 teaspoon cayenne pepper</li>
				<li>1 salmon steak per person</li>
				<li>1/2 teaspoon fresh lemon juice</li>
				<li>Roasting potatoes</li>
