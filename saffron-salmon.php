		<div id="instructions">
			<h2>Instructions</h2>
			<p><strong>Pre-prep (25min):</strong></p>
			<p class="notice">Can be done up to 4h before - or can be done in between the potatoes going in the oven and the cooking of the salmon</p>
			<ol>
				<li>Combine mussels with white wine in a saucepan on medium head. Cover and cook for approx 4 minutes and stir occasionally until mussels open</li>
				<li>Transfer mussels to a bowl, pour liquid from the saucepan into a measuring jug and add safron, stir and leave to stand for 15 minutes</li>
				<li>Transfer liquid back to a saucepan, add cream, tomatoes, garlic, bay leaf and cayenne. Simmer over medium head until sauce thickens (approx 5 minutes), season with salt and pepper</li>
				<li>Cover sauce and mussels seperately and put in fridge</li>
			</ol>
			<p><strong>Prepare (5min):</strong></p>
			<ol>
				<li>Peel potatoes, slice into strips, put into pyrex</li>
				<li>Put salmon steaks skin side down on kitchen towel for 10 minutes to dry out</li>
			</ol>
			<p><strong>Cooking (60min):</strong></p>
			<ol>
				<li>Put potatoes in the oven for 60mins (also needs tossing half way through)</li>
				<li>Put veggies on</li>
				<li>Heat a pain and put some oil in, once hot add salmon skin side down, reduce head instantly and leave for a couple of minutes</li>
				<li>Heat up the sauce, add mussels and heat for approx 2 minutes adding in the lemon juice</li>
			</ol>
