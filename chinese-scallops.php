				<li>Put the vegetables on</li>
				<li>Finely slice the leaves and stalks of a small bunch of coriander and sprinkle over the rice, sweet chilli on top, put lid on and take off the heat</li>
				<li>Put a good lug of olive poil in the frying pan, add the scallops scored side down, 2.5 minutes face down, turn them all overcook for 30 seconds.
				<li>Crush 1 unpeeled garlic over, 1/2 a lemon juice, tiny drizzle of honey and 2 small knobs of butter</li>
				<li>Take off the heat, when cooled slightly and sticky sprinkle over a small bunch of corriander</li>
