			<h2>Instructions</h2>
			<p><strong>Prepare (3min):</strong></p>
			<ol>
				<li>Trim fat off duck</li>
				<li>Score diagonal lines JUST in the fat</li>
				<li>All dressing ingredients into a bowl: Vinegar, soy, sesame, sugar and mix well</li>
			</ol>
			<p><strong>Cooking (45min):</strong></p>
			<ol>
				<li>Stir honey and soy sauce in a bowl, add duck breasts and mix thoroughly, cover and leave to marinade (30mins)</li>
				<li>Peel and segment oranges and put into a bowl </li>
				<li>Cook duck, 5-6 min skin side down, turn and repeat, turn heat off, cover and rest for 5-7 minutes</li>
				<li>Salad on plate, oranges on top, slice duck, add to plate, add chives on top</li>
			</ol>
