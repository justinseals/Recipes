		</div><!--Ingredients End-->
		
		<div id="instructions">
			<h2>Instructions</h2>
			<p><strong>Prepare (3min):</strong></p>
			<ol>
				<li>Slice bacon, halve the kidneys (removing any white sinew), finely slice the mushrooms and cut up the rosemary leaves</li>
				<li>Cube half an onion</li>
				<li>Boil the kettle</li>
			</ol>
			<p><strong>Cooking (20min):</strong></p>
			<ol>
				<li>Put some oil in the saucepan and add the onion, stir for approximately 1 minute and then add the rice, stir for a further 1 minute</li>
				<li>Add exactly double the water of rice you put in (so 2 full mugs), season with salt, crack 2 cardamon pods in, turn to the lowest heat and put lid on for 18 minutes</li>
				<li>Add the bacon to the frying pan with some oil, add the rosemary, the kidneys, mushrooms and add salt and pepper, stir well</li>
				<li>Put on the vegetables</li>
				<li>Add the wine to the frying pan, after 30 seconds pour in the 150ml of cream and mustard, stir well - approx 5 minutes</li>
				<li>Fry the steaks, 15 seconds either side and then 2 minutes either side (depending on thickness of the steak)</li>
				<li>Add a touch of water to the sauce and take off the heat</li>
			</ol>
