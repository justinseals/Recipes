				<li>4 x decent steak</li>
				<li>4 x rashers of bacon</li>
				<li>200g pork kidney</li>
				<li>8 medium chestnut mushrooms</li>
				<li>Swig of white wine</li>
				<li>150ml single cream</li>
				<li>1 tablespoon of English mustard</li>
				<li>Sprig of rosemary</li>
				<li>1 mug of basmati rice</li>
				<li>Half a red onion</li>
				<li>2 cardomon pods</li>
				<li>Vegetables</li>
			</ul>
			<p><strong>Utensils:</strong></p>
			<ul>
