			<p><strong>Stuffing (20min):</strong></p>
			<p class="notice">Can make the day before and refridgerate &amp; mushrooms can be swapped with dried fruit and/or chunks of apple</p>
			<ol>
				<li>Blitz the bread slices into breadcrumbs</li>
				<li>Add Matzah meal to the breadcrumbs, lightly salt and mix well</li>
				<li>Cut onion in half and thinly slice, add to a frying pan with oil and chicken fat and chopped garlic on a low heat and sweat until soft (approx 5 minutes)</li>
				<li>Cube mushrooms small and add to frying pan and raise heat</li>
				<li>Season with sage, thyme and freshly ground black pepper</li>
				<li>Add large swig of sherry and chicken stock</li>
				<li>Simmer for 3-4 minutes and then add everything in the frying pan into to bread and Matzah meal mix, then mix thoughrouly</li>
			</ol>
			<p><strong>Prepare (10min):</strong></p>
			<ol>
				<li>Peel potatoes, slice into strips, put into pyrex</li>
				<li>Cube onion</li>
				<li>Pour boiling water inside chicken, clean out insides and pluck any large feathers still attached</li>
				<li>Pour boiling water on the outside of the chicken so skin tightens against the chicken flesh</li>
			</ol>
			<p><strong>Cooking (75min):</strong></p>
			<ol>
				<li>Put cubed onion and thyme into chicken, on a baking tray with a cm of water underneath and into the oven for 75minutes (needs basting half way through)</li>
				<li>Put potatoes in the oven for the same period of time (also needs tossing half way through)</li>
