			<p><strong>Prepare (2min):</strong></p>
			<ol>
				<li>Cut onion in half and then slice thinly</li>
				<li>Heat oven to 160 degrees without fan</li>
			</ol>
			<p><strong>Cooking (180mins):</strong></p>
			<ol>
				<li>Put 4 eggs into a saucepan of cold water, bring to the boil and then simmer for 10 minutes, drain and replace with cold water and leave to cool</li>
				<li>Layer 400g of chicken livers in Pyrex, springly lightly with salt, sprinkle half the onion slices and repeat once</li>
				<li>Spread chicken fat on top</li>
				<li>Cover with foil and put in oven for 2 hours</li>
				<li>Take out of the oven and allow to cool</li>
				<li>Mince hard boiled eggs, take out 1 tablespoon to set aside and add the rest to a mixing bowl</li>
				<li>Mince the livers and onions, add to mixing bowl and mix well</li>
				<li>Put into serving dish, sprinle the remaining egg over the top, cover with cling film when cool and put in the fridge overnight</li> 
				<li class="notice">Remove from fridge 1 hour before serving</li>
			</ol>
